        document.addEventListener('DOMContentLoaded', function () {
    var checkoutDataJSON = sessionStorage.getItem('checkoutData');

    if (checkoutDataJSON) {
        var checkoutData = JSON.parse(checkoutDataJSON);
        document.getElementById('day_details').innerHTML = checkoutData.day_value;
    }
});

        function showDiv(divId, button) {
            var divs = document.getElementsByClassName("lower");
            for (var i = 0; i < divs.length; i++) {
                divs[i].style.display = "none";
            }

            var buttons = document.getElementsByTagName("button");
            for (var i = 0; i < buttons.length; i++) {
                buttons[i].classList.remove("active");
            }

            document.getElementById(divId).style.display = "block";
            button.classList.add("active");
        }
        function showSeatDetails(boxId) {
    var seatDetails = document.getElementById(boxId).innerHTML;
    document.getElementById("seatDetails").innerHTML = seatDetails;

    updateTotalPrice();

    var modal = document.getElementById("myModal");
    modal.style.display = "block";
}
var seatsInput = document.getElementById("seatsInput");

seatsInput.addEventListener("input", function() {
    updateTotalPrice();
});
function updateTotalPrice() {
    var seatCount = document.getElementById("seatsInput").value;
    var seatPrice = 1000;

    var totalPrice = seatCount * seatPrice;
    document.getElementById("totalPrice").innerHTML = totalPrice + " pkr";
}
function openCheckout() {
    var selectedSeats = document.getElementById('seatsInput').value;
    var totalPrice = document.getElementById('totalPrice').innerHTML;
    var boxElement = document.getElementById('seatDetails');
    var clonedBox = boxElement.cloneNode(true);
    var elementsToRemove = clonedBox.querySelectorAll('.l, .click');
    elementsToRemove.forEach(function (element) {
        element.parentNode.removeChild(element);
    });
    var seatDetails = clonedBox.innerHTML;
    console.log(seatDetails);

    var checkoutData = {
        selectedSeats: selectedSeats,
        seatDetails: seatDetails,
        totalPrice: totalPrice,
    };

    var checkoutDataJSON = JSON.stringify(checkoutData);
    sessionStorage.setItem('checkoutData', checkoutDataJSON);

    window.location.href = 'http://127.0.0.1:8000/checkout';
}

function closeModal() {
    var modal = document.getElementById("myModal");
    modal.style.display = "none";
}

window.onclick = function(event) {
    var modal = document.getElementById("myModal");
    if (event.target == modal) {
        modal.style.display = "none";
    }
}
